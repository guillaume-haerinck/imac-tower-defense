#include <NsCore/Package.h>
#include <NsRender/GLFactory.h>
#include <NsRender/Texture.h>

#include "GLRenderDevice.h"


using namespace Noesis;
using namespace NoesisApp;


////////////////////////////////////////////////////////////////////////////////////////////////////
Ptr<RenderDevice> GLFactory::CreateDevice()
{
    return *new GLRenderDevice();
}

////////////////////////////////////////////////////////////////////////////////////////////////////
Ptr<Texture> GLFactory::WrapTexture(GLuint object, uint32_t width, uint32_t height, uint32_t levels,
    bool isInverted)
{
    return GLRenderDevice::WrapTexture(object, width, height, levels, isInverted);
}

